LOCALES = {"en": _EN, "zh": _ZH}


def translate(locale: str, key: str, **kwargs: Any) -> str:
    """Translate a key with English fallback and optional interpolation."""
    dictionary = LOCALES.get(locale, _EN)
    text = dictionary.get(key, _EN.get(key, key))
    return text.format(**kwargs) if kwargs else text


def t(key: str, **kwargs: Any) -> str:
    """Translate using the active Streamlit session locale."""
    return translate(st.session_state.get("locale", "en"), key, **kwargs)


def enum_label(group: str, value: str) -> str:
    """Translate a persisted enum while preserving unknown future values."""
    return t(group + "." + value)
